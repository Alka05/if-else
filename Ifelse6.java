package ifelse;

public class Ifelse6 {

	public static void main(String[] args) {
		// In num and max variable we store value
		int num = 100 - 33;
		int max = 55;
		// checking num equal to max or not
		if (num == max) {
			System.out.println("barbar hai");
		} else {
			System.out.println("barbar nhi hai");
		}

	}

}
