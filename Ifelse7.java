package ifelse;

public class Ifelse7 {

	public static void main(String[] args) {
		// In max and num variable store value
		int max = 44 + 200;
		int num = 123;
		// checking max equal to num or not
		if (max == num) {
			System.out.println("barbar hai");
		} else {
			System.out.println("barbar nahi hai");
		}

	}

}
