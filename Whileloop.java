package ifelse;

public class Whileloop {

	public static void main(String[] args) {
		// store 0 value in max variable
		int max = 0;
		// here loop working 10 times
		while (max < 10) {
			// print alka 10 times
			System.out.println("alka");
			// increment in max variable
			max = max + 1;

		}
	}

}
