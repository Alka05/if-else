package ifelse;

public class Whileloop1 {

	public static void main(String[] args) {
		// number variable store 2 because will be print from 2
		int number = 2;
		// print 1 to 100 between even numbers which divisible by 2
		while (number <= 100) {
			System.out.println(number);
			// increment 2 because continue loop
			number += 2;

		}
	}

}
